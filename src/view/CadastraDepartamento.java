    public void readJtable(){
        DefaultTableModel modelo = (DefaultTableModel) table_departamentos.getModel();
        DepartamentoDAO dpdao = new DepartamentoDAO();
        modelo.setNumRows(0);
        
        for(Departamento dp: dpdao.read()){
            modelo.addRow(new Object[]{
                dp.getId(),
                dp.getDescricao()
            });
        }
    }

