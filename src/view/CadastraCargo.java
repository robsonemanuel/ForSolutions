    public void readJtable(){
        DefaultTableModel modelo = (DefaultTableModel) table_cargos.getModel();
        CargoDAO cargodao = new CargoDAO();
        modelo.setNumRows(0);
        
        for(Cargo crg: cargodao.read()){
            modelo.addRow(new Object[]{
                crg.getId(),
                crg.getNome(),
                crg.getSalario(),
                crg.getDpto()
            });
        }
    }

