    public boolean validaCampo(){
        boolean check = true;
        if(edt_cargo.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Preencha o cargo");
            check = false;
        }else if(edt_salario.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Preencha o Salário");
            check = false;
        }
        return check;
    }
    private void btn_cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cadastrarActionPerformed
        if(validaCampo()){
            CargoDAO cargodao = new CargoDAO();
            DepartamentoDAO dpdao = new DepartamentoDAO();
            Cargo crg = new Cargo();
            float salario = Float.parseFloat(edt_salario.getText());
            String dept_selecionado = (String) combo_departamento.getSelectedItem();
            if(dept_selecionado.equalsIgnoreCase("selecione o departamento")){
                JOptionPane.showMessageDialog(null,"Selecione um Departamento !");
            }else{
                Departamento d;
                d = dpdao.select(dept_selecionado);       
                crg.setNome(edt_cargo.getText());
                crg.setSalario(salario);
                crg.setDpto(d.getId());
                if(cargodao.insert(crg)){
                    readJtable();
                    edt_cargo.setText("");
                    edt_salario.setText("");            
                }
            }
        }
        
    }//GEN-LAST:event_btn_cadastrarActionPerformed

    private void btn_excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_excluirActionPerformed
        if(validaCampo()){
            CargoDAO cargodao = new CargoDAO();
             Cargo crg = new Cargo();        
            int id = 0;
            if(table_cargos.getSelectedRow() != -1F){            
                 id = ((int)table_cargos.getValueAt(table_cargos.getSelectedRow(),0));
            }

              if(id > 0){
                if(cargodao.delete(id)){
                    readJtable();
                    edt_cargo.setText("");
                    edt_salario.setText("");
                }
            }
        }
    }//GEN-LAST:event_btn_excluirActionPerformed

    private void btn_alterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_alterarActionPerformed
        if(validaCampo()){
            CargoDAO cargodao = new CargoDAO();
            DepartamentoDAO dpdao = new DepartamentoDAO();
            Cargo crg = new Cargo();
            float salario = Float.parseFloat(edt_salario.getText());
            String dept_selecionado = (String) combo_departamento.getSelectedItem();  
            Departamento d;
            d = dpdao.select(dept_selecionado);
            int idDpt = d.getId();
            int id = 0;        
            if(table_cargos.getSelectedRow() != -1F){            
                 id = ((int)table_cargos.getValueAt(table_cargos.getSelectedRow(),0));            
            }       

            if(dept_selecionado.equalsIgnoreCase("selecione o departamento")){
                    JOptionPane.showMessageDialog(null,"Selecione um Departamento !");
            }else{
                if(id > 0){
                    if(cargodao.update(id,edt_cargo.getText(),salario,idDpt)){
                        readJtable();
                        edt_cargo.setText("");
                        edt_salario.setText("");
                    }
                }
             }
        }
    }//GEN-LAST:event_btn_alterarActionPerformed

