    public void readJTable(){
       DefaultTableModel modelo = (DefaultTableModel) jTable3.getModel();
       modelo.setNumRows(0);
       ContasAPagarDAO cDAO = new ContasAPagarDAO();
       for(ContaPagar c: cDAO.read()){
          modelo.addRow(new Object[]{
             c.getCodigo(),
             c.getDesc(),
             c.getValor(),
             c.getData_venc()
          });
       }
    }

