    private void btn_pesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_pesquisarActionPerformed
        int num = Integer.parseInt(edt_num_os.getText());
        OrdemServicoDAO osdao = new OrdemServicoDAO();
        List<Ordem_Servico> list_os = osdao.selectAll(num);        
        DefaultTableModel modelo = (DefaultTableModel) table_os.getModel();        
        modelo.setNumRows(0);
        
        for(Ordem_Servico os: osdao.selectAll(num)){
            modelo.addRow(new Object[]{
                os.getDescri_prod(),
                os.getDescri_def(),
                os.getData(),
                os.getNome(),
                os.getFinalizada()
            });
        }
        
       
    }//GEN-LAST:event_btn_pesquisarActionPerformed

