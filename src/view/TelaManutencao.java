    public void readJtable(){
        DefaultTableModel modelo = (DefaultTableModel) table_os.getModel();
        DefaultTableModel modelo2 = (DefaultTableModel) table_produto.getModel();
        OrdemServicoDAO osdao =  new OrdemServicoDAO();
        EstoqueProdutoDAO pdao = new EstoqueProdutoDAO();
        modelo.setNumRows(0);
        modelo2.setNumRows(0);
        
        for(Ordem_Servico os: osdao.read()){
            modelo.addRow(new Object[]{
                os.getId(),
                os.getDescri_prod(),
                os.getDescri_def(),
                os.getNome()
            });
        }
        
        for(Produto p: pdao.readOrderByAsc()){
            modelo2.addRow(new Object[]{
                p.getId(),
                p.getDescricao()                
            });
        }
    }

