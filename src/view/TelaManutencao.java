    private boolean verifica(){
        boolean check = true;
        if(edt_nos.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Preencha o número da O.S.");
            check = false;
        }
        if(edt_nmTecnico.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Preencha o nome do técnico");
            check = false;
        }
        if(edt_nmCliente.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Preencha o nome do Cliente");
            check = false;
        }
        if(edt_dt_inicio.getText().equals("  /  /    ")){
            JOptionPane.showMessageDialog(null,"Preencha a data de inicio");
            check = false;
        }
        if(edt_dt_fim.getText().equals("  /  /    ")){
            JOptionPane.showMessageDialog(null,"Preencha a data de fim");
            check = false;
        }
        if(text_obs.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Preencha o campo de Obersavação");
            check = false;
        }
        return check;
    }
    
