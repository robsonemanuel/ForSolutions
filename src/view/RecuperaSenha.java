    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        RecuperaSenhaDAO rec = new RecuperaSenhaDAO();
        String email = edt_email.getText();
        String cpf = edt_cpf.getText();
        if(rec.ConfereEmail(email,cpf)){            
            if(rec.GeraCod(cpf)){                
                this.dispose();                
                JOptionPane.showMessageDialog(null,"Enviamos um código para seu email.\n acesse o sistema novamente com esse código enviado !");
                JavaMailApp sendMail = new JavaMailApp();
                sendMail.EnviaMail(email, cpf);
                
            }else{
                JOptionPane.showMessageDialog(null,"Não conseguimos gerar a nova senha");
            }
                        
        }else{
            JOptionPane.showMessageDialog(null,"Dados  Inválidos");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

