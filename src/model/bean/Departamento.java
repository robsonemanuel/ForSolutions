package model.bean;



/**
 *
 * @author LUCAS
 */
public class Departamento {
    private int id;
    private String descricao;
    

    public Departamento() {
    }
    
 


    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }
    
    
}
