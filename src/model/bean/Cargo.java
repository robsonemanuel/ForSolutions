package model.bean;

/**
 *
 * @author LUCAS
 */
public class Cargo {
    private String nome;
    private float salario;

    public Cargo() {
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public float getSalario() {
        return salario;
    }

    public void setSalario(float salario) {
        this.salario = salario;
    }
    
    
}
